package ex_04_Operators;

public class Lab014_Arithmetic_Operators {
    public static void main(String[] args) {
       /* Arithemetic Operator
                + - Addition
                - Subtraction
                * Multiplication
                / Division
                % Modulus*/

        float a = 20.00f;
        int b = 3;
        int c = 3;
        System.out.println(a+b); //23.00
        System.out.println(a-b); //17.00
        System.out.println(a*b); //60.00
        System.out.println(a/b); //6.665
        System.out.println(a%c);//2.0

        System.out.println("a+b"); //a+b
        System.out.println(a+b);//23.00
    }
}
