package ex_10_For_Loop;

public class Lab42_For_If_LOOP {
    public static void main(String[] args) {

        for(int shruti=0;shruti<18;shruti++)
        {
            // 0 to 17, 18 times
            if(shruti>15)
            {
                System.out.println("Iphone gift to her by dad");
            }else
            {
                System.out.println("No gift, only candy");
            }
        }
    }
}
